# Faça um programa que peça 4 notas e mostre a media
# "A media das quatro notas foi: X"

nota1 = input("Digite nota 1: ")
nota2 = input("Digite nota 2: ")
nota3 = input("Digite nota 3: ")
nota4 = input("Digite nota 4: ")

soma = int(nota1) + int(nota2) + int(nota3) + int(nota4)
divisao = soma / 4

print("A media das 4 notas foi : " + str(divisao))


